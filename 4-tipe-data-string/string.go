package main

import "fmt"

// mencetak tipe data dengan string
func main() {
	fmt.Println("ata")
	fmt.Println("muchamad hatta")

	fmt.Println(len("ata"))          // len untuk menampilkan panjang data
	fmt.Println("muchamad hatta"[0]) //untuk menampilkan jumlah byte string ke 0
	fmt.Println("muchamad hatta"[1]) //untuk menampilkan jumlah byte string ke 1

}
